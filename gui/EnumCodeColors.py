from enum import Enum
from termcolor import colored, cprint


class CodeColors( Enum ):

    # folgende attribute speichern die möglichen
    # farben, die der spiele für das spiel auswählen,
    # bzw. verwenden kann
    
    BLACK   = "black"
    RED     = "red"
    GREEN   = "green"
    YELLOW  = "yellow"
    BLUE    = "blue"
    MAGENTA = "magenta"
    CYAN    = "cyan"
    WHITE   = "white"
